# Request two integer numbers from user.
# Output on one line expression with sum of numbers,
# and on another line - expression with product of numbers:

first = int(input("Enter a integer number, please: "))
second = int(input("Enter one more integer number, please: "))
print(first, '+', second, '=', first+second)
print(first, '*', second, '=', first * second)
